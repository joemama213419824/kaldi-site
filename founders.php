                <h2>Francis Warun Scopas</h2>
                <p>Mr. Francis Warun was born in Kenya where his parents were refugees during the first civil war in Sudan (1955-1972). After the Addis Ababa Agreement was signed on 3rd March 1972, Francis, his parents and siblings returned to South Sudan (then Sudan) and settle at Lainya which is located 100 kilometres to the west of Juba. He was enrolled with his siblings at Lainya Primary School where he passed to intermediate secondary school. After his father died in Lainya in the mid-1980s, Francis and his elder siblings relocated to settle in Juba where continued his education. Francis further relocated to Khartoum when the second civil war that broke out in 1982 intensified with bombardment of the city. He was accepted to the University of Juba and studied education. After graduation, Francis became a secondary school teacher in Khartoum. He earned a scholarship to study language development at the University of Oslo, Norway.
                    After completing his Master of Arts, Francis returned to South Sudan where he worked with an NGO that was committed to language development. While a post-graduate student in Norway in 2010, Francis consulted Dr. Laila Lokosang through email exchanges to seek advice on his on his research topic which was on the development of the Bari language. At the time Laila  was compiling his pioneering work – The Concise Pojulu-English Dictionary – which was published in 2010 by Xlibris. Francis obtained a copy of the Dictionary and was inspired. Both happened to be blood relatives. 
                    The two returned to Juba in 2007 and 2009 where they met after nearly 15 years. They discussed the idea of developing what used to be known as ‘the Bari Language’. Francis was by then working for a non-Governmental Organisation committed to language and culture development. They agreed to expand the idea to include other interested members of the ‘Bari-speaking’ communities who worked at the Central Equatoria State Ministry of Education. Francis then established contact with Mr. Patrick Ladu, Mr. Francis Lokosang and Mr. Henry Duku and arranged a meeting the Juba Unity Park. That’s where the idea of developing what later came to be known the Karo Language was nurtured. 
                    Francis died a tragic death in the hands of cruel soldiers on 8th July 2016 when he found himself caught in the dog fight between two rival armies in front of the State House. He was trying to run home to his family after attending last funeral prayers of a community member and his car was stopped by a group of government armed men. Francis is survived by his widow, Ya’bang, and three children. He surely left behind a legacy of championing the establishment of KALDI and the development of the Karo Language. May his soul rest in eternal peace. 
                </p>
            </div>

            <div class="founder-card">
                <img src="images/founder1.jpeg" alt="Founder 2">
                <h2>Dr. Laila Lokosang</h2>
                <p>Laila Lokosang was born and grew up at Lainya County of South Sudan. In 1968, Laila was enrolled at Lainya Primary School where he completed the Primary Leaving Certificate and was accepted to Maridi 1 Boys Junior Secondary School in 1975. After studying in Maridi for only one academic year, Laila transferred to continue at Juba 1 Junior Secondary School, which was later renamed “Sudan-Kuweit Friendship School” since it was constructed with a grant from the State of Kuweit. Obtaining first position in the second and third years at the school, Laila earned a scholarship to study at White Nile Boys Secondary School in Ed-Duweim in Sudan. He passed the Sudan Secondary School among the top three students and was accepted at the University of Juba College of Social and Economic Studies where he majored in Statistics. He graduated best overall student in his specialization on 16 December 1986. 
                    In May 1987, Laila started his career as Assistant Lecturer of Business Statistics and Numeracy at the then Regional Accountancy Training Centre; an institution established by the then Regional Ministry of Finance and Economic Planning of the Government of Southern Sudan. After exactly one year of civil service, Laila transferred to serve in the Ministry of Health of what used to be called Equatoria State. He was appointed as Chief Biostatistician. In 1992 while undergoing short professional courses in computing, data management, project planning, management and evaluation, Laila applied to become a UN Volunteer. He was recruited and sent to Malawi where he worked in the Ministry of Health as Health Information Systems Specialist for seven years, then for another five years as consultant in Information Management Systems, database design and management, and statistical analysis.
                    In 2006 Laila returned to his native country of South Sudan to serve with the then United Nations Mission in Sudan as Information Management Officer and, after only serving for six months, Laila moved to serve in the position of Director for Monitoring and Evaluation at the then South Sudan Commission for Census, Statistics, Monitoring and Evaluation (SSCCSE). That is the time when Laila met with late Francis Warun and others and co-founded the Karo Language Development Institute (KALDI, which initially known as the Greater Bari Language Development Association). Since 2012, Laila has been serving at the African Union Commission at different capacities. 
                    Laila is a holder of a doctoral degree in Statistics, master of science in Food Security and a Bachelor of Science in Statistics and Demography. Besides his professional career, Laila is a keen learner of languages. Apart from authoring and having published his maiden The Pojulu-English Dictionary, Laila has since 2019 compiled the Karo-English Dictionary which is undergoing professional linguistic review. Also in 2019, Laila joined forces with Dr. Henry Wani Rondyang to revive the initiative of GBLDA. The project which assembled close to 250 members by 2023, resolved to rename the initiative Karo Language Development Institute. This was done through the Declaration of the Karo People dated 1st August 2019. Subsequently, the project of compiling the first edition of the Karo-English Dictionary was endorsed and work has since continued. Laila has since been leading these efforts.  
                </p>
